public class ing0928 {

    public static void main(String[] args) {
	    fun();
    }
    public static void fun(){
        int n = 6;
        for(int i = 0; i < n ; i++ ){
            for( int j = 0 ; j <= i ; j ++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
